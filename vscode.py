from turtle import *

def draw_vscode_logo():
    speed(10)
    bgcolor("white")
    color("#0078B8")  # VS Code 蓝色
    pensize(3)

    penup()
    goto(-100, -20)
    pendown()
    begin_fill()
    seth(42)
    fd(260)
    seth(270)
    fd(70)
    seth(218)
    fd(212)
    seth(137)
    fd(37)
    end_fill()

    color("#0087D5")
    penup()
    goto(-100, 50)
    pendown()
    begin_fill()
    seth(318)
    fd(260)
    seth(90)
    fd(70)
    seth(142)
    fd(212)
    seth(223)
    fd(37)
    end_fill()

    color("#21A6F2")
    penup()
    goto(94, 155)
    pendown()
    begin_fill()
    seth(-30)
    fd(60)
    seth(270)
    fd(218)
    seth(210)
    fd(60)
    seth(90)
    fd(240)
    end_fill()

    hideturtle()
    done()

draw_vscode_logo()